/*4 - Escreva um programa Java que use um laço para somar os números de 1 a 100. */
package Aulas.Aula02;

public class Exercicio04 {
    public static void main(String[] args) {
        int soma = 0;
        for(int i=0; i<100; i++ ){
            soma=soma+i;
        }
        System.out.println(soma+"\n");
        
    }
    
}
